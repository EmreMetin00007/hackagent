"""
mcp-reasoning — Reverse-Proxy + WAF Intelligence (a+b+c) offline testleri:
fingerprint_waf, classify_response, discover_origin, adaptive_evasion_step +
WAF-aware evolve_payload. Deterministik, canlı istek/network YOK.
"""
import json
from conftest import load_server


def _r():
    return load_server("reasoning")


# ───────────────────── (a) fingerprint_waf ─────────────────────
def test_fingerprint_cloudflare_cdn():
    mod = _r()
    out = json.loads(mod.fingerprint_waf(
        headers="Server: cloudflare\nCF-RAY: 7a2b\ncf-cache-status: DYNAMIC",
        body="Attention Required! | Cloudflare", status_code=403))
    assert out["vendor"] == "cloudflare"
    assert out["is_cdn_reverse_proxy"] is True
    assert out["advise_origin_discovery"] is True
    assert "case_swap" in out["recommended_operators"]


def test_fingerprint_modsecurity_not_cdn():
    mod = _r()
    out = json.loads(mod.fingerprint_waf(
        body="This error was generated by Mod_Security", status_code=406))
    assert out["vendor"] == "modsecurity"
    assert out["is_cdn_reverse_proxy"] is False
    assert out["advise_origin_discovery"] is False


def test_fingerprint_unknown():
    mod = _r()
    out = json.loads(mod.fingerprint_waf(headers="Server: nginx", body="hello", status_code=200))
    assert out["waf_detected"] is False
    assert out["vendor"] == "unknown"


